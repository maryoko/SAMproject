    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        UsuarioControle usu = new UsuarioControle();
        Usuario usuario= null;
        if (txtlogin.getText().equals("") || txtsenha.getPassword().toString().equals("") || 
                txtnome.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Favor prencher os campos.");
        }else{
            try {
                usu.incluir(txtlogin.getText(), txtsenha.getPassword().toString(), txtnome.getText());
   
               JOptionPane.showMessageDialog(null, "Usuário incluído com sucesso.");
            } catch (Exception e) {
               JOptionPane.showMessageDialog(null, "Erro na inclusão do usuário. " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

